package com.company;

public class BlogPostThing {
  private String title;
  private String author;
  private String publicationDate;
  private String text;

  public BlogPostThing(String title, String author, String publicationDate, String text) {
    this.title = title;
    this.author = author;
    this.publicationDate = publicationDate;
    this.text = text;
  }
}